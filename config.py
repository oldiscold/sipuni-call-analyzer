"""
Конфигурация проекта: маппинг внутренних номеров на имена менеджеров.
"""

# Маппинг внутренних номеров на имена менеджеров.
# Замени значения на реальные имена.
MANAGER_MAP: dict[str, str] = {
    "205": "Менеджер 205",
    "206": "Менеджер 206",
    "219": "Менеджер 219",
}


def get_manager_name(short_num: str | None) -> str:
    """Возвращает имя менеджера по внутреннему номеру."""
    if not short_num:
        return "Неизвестный менеджер"
    return MANAGER_MAP.get(short_num, f"Менеджер (внутр. {short_num})")
